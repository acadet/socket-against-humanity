function random(list) {
    return list[Math.round(Math.random() * (list.length - 1))];
}

function engineName(value) {
    var trimmedValue = value.trim(), outcome = "";

    for (var i = 0; i < trimmedValue.length; i++) {
        var c = trimmedValue[i];

        if (c == " " ) {
            outcome += "-";
        } else if (c == c.toUpperCase() && i > 0 && trimmedValue[i-1] != " ") {
            outcome += "-" + c.toLowerCase();
        } else {
            outcome += c.toLowerCase();
        }
    }

    return outcome;
}

module.exports = {
    generateName: function() {
        var color = engineName(random(COLORS));
        var animal = engineName(random(ANIMALS));
        return color + "-" + animal;
    }
};